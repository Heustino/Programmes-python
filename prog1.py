# Cette boucle affiche les nombres de 10 à 14, un par ligne.
for i in range(10, 15):
  print(i)

# Cette boucle affiche les nombres de 20 à 39, un par ligne, avec un pas de 2.
for i in range(20,40,2):
  print(i)

# Cette boucle affiche les nombres pairs de 0 à 19, un par ligne.
for i in range(20):
  if i%2 == 0:
    print(i)

# Cette boucle affiche les nombres impairs de 0 à 19, un par ligne.
for i in range(20):
  if i%2 != 0:
    print(i)

